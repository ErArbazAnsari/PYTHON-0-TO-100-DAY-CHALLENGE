# Type casting in python
# Type casting is a process of converting one data type to another data type
# Type casting is also known as type conversion
# Type casting is done by using predefined functions
# Type casting is done by using constructor functions

# Example of type casting in python
# Example of explicit type casting in python
num = 10
print(num)
print(str(num), type(str(num)))
print(float(num), type(float(num)))

# Example of implicit type casting in python
num1 = 10
num2 = 20
print(num1 + num2)
print(num1 + float(num2))
print(str(num1) + str(num2))
print(num1 + int(num2))
