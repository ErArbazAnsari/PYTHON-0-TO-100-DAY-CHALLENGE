# Operations of Strings in python

myString = "Hello World"
print(myString)
print(myString[0:5])
print(myString[:])

# We can find the length of the string using len() function
# example of len() function
print(len(myString))
print(myString[0:11])
# print(myString[11]) //This will give an error because the index is out of range
print(myString[10])

print(myString[0:-6])
print(myString[-1:0])

