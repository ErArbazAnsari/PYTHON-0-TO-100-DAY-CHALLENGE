# Code	    Result
# -------------------------
# \'	    Single Quote
# \\	    Backslash
# \n	    New Line
# \r	    Carriage Return
# \t	    Tab
# \b	    Backspace
# \f	    Form Feed
# \ooo	    Octal value
# \xhh	    Hex value


print("My name is Arbaz Ansari")
print("My name is 'Arbaz Ansari'")
print('My name is "Arbaz Ansari"')
print("My name is \\Arbaz Ansari\\")
print("My name is Arbaz Ansari\n")
print("My name is \rArbaz Ansari")
print("My name is \tArbaz Ansari")
print("My name is \b\bArbaz Ansari")
print(
    "My name is Arbaz Ansari\f"
)  # Form Feed is used to print the next line in the same line.
print("Computer Science Student.")

print("\oooA\166")  # Octal value is used to print the ASCII value of the character.

print("\110\105\114\114\117\040\127\117\122\114\104")

print("\x48\x49\x4C\x4C\x55\x20\x57\x4F\x52\x4C\x44")  # Hex value is used to print the ASCII value of the character. Explanation of the above code: 48 is the ASCII value of H, 49 is the ASCII value of I, 4C is the ASCII value of L, 4C is the ASCII value of L, 55 is the ASCII value of U, 20 is the ASCII value of Space, 57 is the ASCII value of W, 4F is the ASCII value of O, 52 is the ASCII value of R, 4C is the ASCII value of L, 44 is the ASCII value of D.